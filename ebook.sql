#
# Structure for the `users` table : 
#

CREATE TABLE `users` (
  `id` int(11) NOT NULL auto_increment,
  `username` varchar(20) collate utf8_bin default NULL,
  `sex` varchar(4) collate utf8_bin default NULL,
  `password` varchar(100) collate utf8_bin default NULL,
  `phone` varchar(50) collate utf8_bin default NULL,
  `balance` double default NULL,
  `email` varchar(100) collate utf8_bin default NULL,
  `grade` int(11) default NULL,
  PRIMARY KEY  (`id`),
  UNIQUE KEY `unemail` (`email`),
  KEY `Fk_grade` (`grade`),
  CONSTRAINT `Fk_grade` FOREIGN KEY (`grade`) REFERENCES `grade` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

#
# Structure for the `shouhuo` table : 
#

CREATE TABLE `shouhuo` (
  `id` int(11) NOT NULL auto_increment,
  `address` varchar(200) collate utf8_bin default NULL,
  `sendee` varchar(20) collate utf8_bin default NULL,
  `telephone` varchar(30) collate utf8_bin default NULL,
  `userID` int(11) default NULL,
  PRIMARY KEY  (`id`),
  KEY `FK_shouHuoUser` (`userID`),
  CONSTRAINT `FK_shouHuoUser` FOREIGN KEY (`userID`) REFERENCES `users` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

#
# Structure for the `orderform` table : 
#

CREATE TABLE `orderform` (
  `id` int(11) NOT NULL auto_increment,
  `orderNumber` bigint(20) default NULL,
  `expressID` int(11) default NULL,
  `userID` int(11) default NULL,
  `shouHuoID` int(11) default NULL,
  `bookNumber` int(11) default NULL,
  `bookID` int(11) default NULL,
  PRIMARY KEY  (`id`),
  KEY `FK_orderformExpress` (`expressID`),
  KEY `FK_orderformShouHuo` (`shouHuoID`),
  KEY `FK_orderformUser` (`userID`),
  KEY `FK_orderformBooks` (`bookID`),
  CONSTRAINT `FK_orderformBooks` FOREIGN KEY (`bookID`) REFERENCES `books` (`id`),
  CONSTRAINT `FK_orderformExpress` FOREIGN KEY (`expressID`) REFERENCES `express` (`id`),
  CONSTRAINT `FK_orderformShouHuo` FOREIGN KEY (`shouHuoID`) REFERENCES `shouhuo` (`id`),
  CONSTRAINT `FK_orderformUser` FOREIGN KEY (`userID`) REFERENCES `users` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

#
# Structure for the `pwdprotection` table : 
