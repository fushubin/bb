#
# Structure for the `booktype` table : 
#

CREATE TABLE `booktype` (
  `id` int(11) NOT NULL auto_increment,
  `typeName` varchar(20) collate utf8_bin default NULL,
  PRIMARY KEY  (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

#
# Structure for the `booktypeson` table : 
#

CREATE TABLE `booktypeson` (
  `id` int(11) NOT NULL auto_increment,
  `typeSonName` varchar(50) collate utf8_bin default NULL,
  `bookTypeID` int(11) default NULL,
  PRIMARY KEY  (`id`),
  KEY `FK_bookSonFather` (`bookTypeID`),
  CONSTRAINT `FK_bookSonFather` FOREIGN KEY (`bookTypeID`) REFERENCES `booktype` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

#
# Structure for the `books` table : 
#

CREATE TABLE `books` (
  `id` int(11) NOT NULL auto_increment,
  `bookName` varchar(100) collate utf8_bin default NULL,
  `author` varchar(50) collate utf8_bin default NULL,
  `price` double default NULL,
  `costPrice` double default NULL,
  `press` varchar(100) collate utf8_bin default NULL,
  `content` varchar(1000) collate utf8_bin default NULL,
  `image` varchar(200) collate utf8_bin default NULL,
  `bookTypeSonID` int(11) default NULL,
  `bookTypeID` int(11) default NULL,
  PRIMARY KEY  (`id`),
  KEY `FK_bookSonType` (`bookTypeSonID`),
  KEY `FK_bookType` (`bookTypeID`),
  CONSTRAINT `FK_bookSonType` FOREIGN KEY (`bookTypeSonID`) REFERENCES `booktypeson` (`id`),
  CONSTRAINT `FK_bookType` FOREIGN KEY (`bookTypeID`) REFERENCES `booktype` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

#
# Structure for the `callboard` table : 
#

CREATE TABLE `callboard` (
  `id` int(11) NOT NULL auto_increment,
  `title` varchar(50) collate utf8_bin default NULL,
  `releaseTime` date default NULL,
  `callContent` varchar(1000) collate utf8_bin default NULL,
  PRIMARY KEY  (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

#
# Structure for the `expresstype` table : 
