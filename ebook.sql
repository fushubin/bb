#
# Structure for the `shoppingcart` table : 
#

CREATE TABLE `shoppingcart` (
  `id` int(11) NOT NULL auto_increment,
  `userID` int(11) NOT NULL,
  `bookID` int(11) default NULL,
  `bookNumber` int(11) default NULL,
  PRIMARY KEY  (`id`),
  KEY `FK_cartBook` (`bookID`),
